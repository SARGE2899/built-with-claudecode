"""
Local Outlier Factor (LOF).

Concept: unlike Isolation Forest and One-Class SVM (which learn one
global notion of "normal"), LOF compares each point's local density
(how tightly packed its k nearest neighbors are) to the local densities
of ITS neighbors. A point sitting in a region much sparser than its
neighbors' regions gets a high outlier score, even if it isn't far away
in absolute distance -- this lets it catch outliers relative to
different-density clusters, not just one global "normal" blob.

Same augmentation approach as `01_isolation_forest.py` (read that
script's docstring for the full rationale): the 2 REAL rows from
`data/loan_data.csv` are used as-is and never modified; a small number
of synthetic neighbor points are generated by jittering around each
real row, plus 2 deliberately extreme synthetic anomaly points. Every
row is labeled with its real/synthetic source below.

With 20 total rows split into two 9-point neighborhoods (real + 8
synthetic each) plus 2 anomalies, n_neighbors must stay well below 20 --
sklearn's default (20) would use literally every other point as a
"neighbor", collapsing the whole point of a LOCAL density comparison. We
use n_neighbors=5, small enough to stay within each of the two
synthetic clusters.
"""

import numpy as np
import pandas as pd
from sklearn.neighbors import LocalOutlierFactor

FEATURE_COLS = ["age", "income", "credit_score", "years_employed", "existing_debt"]


def augment_data():
    df = pd.read_csv("../data/loan_data.csv")
    real_a = df.iloc[0][FEATURE_COLS]
    real_b = df.iloc[1][FEATURE_COLS]

    rng = np.random.default_rng(0)
    jitter_scale = np.array([2, 5, 15, 1, 3])

    def perturb(base_row, n):
        noise = rng.normal(loc=0.0, scale=jitter_scale, size=(n, len(FEATURE_COLS)))
        return np.round(base_row.values.astype(float) + noise).astype(int)

    synthetic_near_a = perturb(real_a, 8)
    synthetic_near_b = perturb(real_b, 8)
    synthetic_anomalies = np.array([
        [90, 200, 300, 45, 95],
        [19, 4, 310, 0, 90],
    ])

    rows = [real_a.values.astype(int), real_b.values.astype(int)]
    rows += list(synthetic_near_a) + list(synthetic_near_b) + list(synthetic_anomalies)
    sources = (
        ["real_A", "real_B"]
        + ["synthetic_near_A"] * len(synthetic_near_a)
        + ["synthetic_near_B"] * len(synthetic_near_b)
        + ["synthetic_anomaly"] * len(synthetic_anomalies)
    )
    return pd.DataFrame(rows, columns=FEATURE_COLS), sources


X, sources = augment_data()

print("=== Augmented dataset (2 REAL rows + synthetic neighbors/anomalies) ===")
print(X.assign(source=sources))

# novelty=False (the default) is the "outlier detection" mode: it only
# ever scores the exact points it was fit on, via fit_predict -- there's
# no separate .predict() for new points in this mode.
model = LocalOutlierFactor(n_neighbors=5)
preds = model.fit_predict(X)
scores = model.negative_outlier_factor_

print("\n=== Local Outlier Factor internal state ===")
print("negative_outlier_factor_ (closer to -1 = as dense as its neighbors; "
      "much more negative = sparser than its neighbors, i.e. an outlier):")

print("\n=== Final scores ===")
for source, pred, score in zip(sources, preds, scores):
    flag = "ANOMALY" if pred == -1 else "normal"
    print(f"  [{source:18s}] score={score:+.4f}  -> {flag}")

n_real_anomalies_caught = sum(
    1 for s, p in zip(sources, preds) if s == "synthetic_anomaly" and p == -1
)
print(f"\n{n_real_anomalies_caught}/2 deliberate synthetic anomalies were correctly "
      "flagged -- notice their scores are far more negative than any "
      "normal point's, since LOF's score is a ratio of densities, not a "
      "bounded probability.")
