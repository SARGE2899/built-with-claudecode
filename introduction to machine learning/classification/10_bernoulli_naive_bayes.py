"""
Bernoulli Naive Bayes classification.

Concept: like Multinomial NB, but assumes each feature is a binary
(present/absent) indicator per class, generated by independent Bernoulli
trials. Before fitting, any non-binary input is binarized against a
threshold (default: x > 0 becomes 1, else 0) -- it's designed for data
that's already boolean (e.g. "does this word appear in the document:
yes/no"), with the threshold a fallback for continuous input, not a
recommended way to use it.

Every feature in this dataset (age, income, credit_score,
years_employed, existing_debt) is a positive number, so BernoulliNB's
default threshold (x > 0) maps ALL of them to 1 for BOTH rows -- that
erases every bit of the actual variation the other classifiers in this
folder use to tell the two rows apart. This is a genuine, instructive
failure of representation, not a hyperparameter mistake to route around:
BernoulliNB is the wrong tool for continuous magnitude data, and its
threshold default silently destroys the input instead of erroring out
about it.
"""

import pandas as pd
from sklearn.naive_bayes import BernoulliNB

df = pd.read_csv("../data/loan_data.csv")
X = df.drop(columns=["approved"])
y = df["approved"]

model = BernoulliNB()  # default binarize=0.0 -> x>0 becomes 1, else 0
model.fit(X, y)

print("=== Bernoulli Naive Bayes internal state ===")
print(f"Classes: {model.classes_}")
print(f"Class log priors: {model.class_log_prior_}")

print("\nWhat the binarization step actually produced (threshold: x > 0):")
binarized = (X > 0).astype(int)
print(binarized)
print("Every feature became 1 for both rows -- binarization destroyed "
      "all the actual variation between them.")

print("\nPer-class feature log-probabilities (feature_log_prob_) -- "
      "identical across classes as a direct result:")
print(pd.DataFrame(model.feature_log_prob_, index=model.classes_, columns=X.columns))

print("\n=== Final predictions ===")
preds = model.predict(X)
probs = model.predict_proba(X)
for i in range(len(X)):
    print(f"  row {i}: true={y.iloc[i]}  predicted={preds[i]}  "
          f"P(class=0)={probs[i][0]:.4f}  P(class=1)={probs[i][1]:.4f}")

print("\nNOTE: feature_log_prob_ is identical across classes above, so "
      "the model has zero real signal to separate them -- any resemblance "
      "between predicted and true labels here is a coincidence of class "
      "ordering/tie-breaking, not evidence the model learned anything. "
      "This is the expected failure mode of feeding continuous magnitude "
      "data into a binary-indicator model.")
