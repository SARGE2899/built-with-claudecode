"""
Multinomial Naive Bayes classification.

Concept: like Gaussian Naive Bayes, but assumes each feature is a count
(or count-like frequency) generated by a multinomial distribution per
class, rather than a continuous Gaussian. It's the classic choice for
text classification (word counts), where "does feature j occur, and how
often" is the natural question -- not "how far is this value from the
class mean".

This dataset's features (age, income, credit_score, years_employed,
existing_debt) are continuous magnitudes, not counts -- feeding them to
MultinomialNB is a conceptual mismatch (there's no natural notion of
"age occurring 25 times"). sklearn has no way to detect that mismatch;
it only requires X >= 0, which happens to hold here, so it returns a
technically-valid answer regardless of whether the representation makes
sense. We use it purely to watch the differing internal mechanics (log
probabilities from summed counts per class) run on the same data
GaussianNB used -- not because it's the right tool for this dataset.

With exactly 1 sample per class, feature_count_ for each class is just
that single row's raw values -- there's no aggregation across multiple
samples to observe.
"""

import pandas as pd
from sklearn.naive_bayes import MultinomialNB

df = pd.read_csv("../data/loan_data.csv")
X = df.drop(columns=["approved"])
y = df["approved"]

model = MultinomialNB()
model.fit(X, y)

print("=== Multinomial Naive Bayes internal state ===")
print(f"Classes: {model.classes_}")
print(f"Class log priors: {model.class_log_prior_}")
print("\nPer-class feature counts (feature_count_) -- with 1 sample per "
      "class, this is literally just that row's raw feature values:")
print(pd.DataFrame(model.feature_count_, index=model.classes_, columns=X.columns))
print("\nPer-class feature log-probabilities (feature_log_prob_):")
print(pd.DataFrame(model.feature_log_prob_, index=model.classes_, columns=X.columns))

print("\n=== Final predictions ===")
preds = model.predict(X)
probs = model.predict_proba(X)
for i in range(len(X)):
    print(f"  row {i}: true={y.iloc[i]}  predicted={preds[i]}  "
          f"P(class=0)={probs[i][0]:.4f}  P(class=1)={probs[i][1]:.4f}")
